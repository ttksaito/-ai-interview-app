const getSystemPrompt = (theme: InterviewTheme): string => {
  switch (theme) {
    case 'life-meaning':
      return LIFE_MEANING_PROMPT;
    case 'job-change':
      return JOB_CHANGE_PROMPT;
    default:
      return LIFE_MEANING_PROMPT;
  }
};

export class ClaudeService {
  private client: Anthropic;
  private model = 'claude-sonnet-4-5';

  constructor(apiKey: string) {
    this.client = new Anthropic({ apiKey });
  }

  async getResponse(
    chatHistory: Message[],
    theme: InterviewTheme = 'life-meaning',
  ): Promise<{
    response: string;
    inputTokens: number;
    outputTokens: number;
  }> {
    const response = await this.client.messages.create({
      model: this.model,
      max_tokens: 1024,
      system: getSystemPrompt(theme),
      messages: chatHistory,
    });

    const textContent = response.content.find((block) => block.type === 'text');
    const responseText = textContent && 'text' in textContent ? textContent.text : '';

    return {
      response: responseText,
      inputTokens: response.usage.input_tokens,
      outputTokens: response.usage.output_tokens,
    };
  }

  async streamResponse(
    chatHistory: Message[],
    theme: InterviewTheme = 'life-meaning',
    onChunk: (text: string) => void,
  ): Promise<{
    fullResponse: string;
    inputTokens: number;
    outputTokens: number;
  }> {
    let fullResponse = '';

    const stream = await this.client.messages.create({
      model: this.model,
      max_tokens: 1024,
      system: getSystemPrompt(theme),
      messages: chatHistory,
      stream: true,
    });

    for await (const event of stream) {
      if (
        event.type === 'content_block_delta' &&
        event.delta.type === 'text_delta'
      ) {
        const text = event.delta.text;
        fullResponse += text;
        onChunk(text);
      }
    }

    // Note: Token usage is not available in streaming mode in this simple implementation
    // For production, you would need to track this differently
    return {
      fullResponse,
      inputTokens: 0, // Would need to calculate or track separately
      outputTokens: 0,
    };
  }
}
